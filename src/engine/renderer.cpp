bool Renderer::init() {
    // Install signal handlers for clean shutdown
    signal(SIGINT, signalHandler);
    signal(SIGTERM, signalHandler);

    if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_JOYSTICK) < 0) {
        SDL_Log("SDL init failed: %s", SDL_GetError());
        return false;
    }

    // Open first joystick (MUOS gamepad)
    if (SDL_NumJoysticks() > 0) {
        SDL_JoystickOpen(0);
    }

#ifdef MUOS_BUILD
    // MUOS: fullscreen, no cursor
    SDL_ShowCursor(SDL_DISABLE);
    m_window = SDL_CreateWindow(
        APP_NAME,
        0, 0,
        SCREEN_W, SCREEN_H,
        SDL_WINDOW_FULLSCREEN_DESKTOP | SDL_WINDOW_SHOWN
    );
#else
    // Desktop: windowed for development
    m_window = SDL_CreateWindow(
        APP_NAME " v" APP_VERSION,
        SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
        SCREEN_W, SCREEN_H,
        SDL_WINDOW_SHOWN
    );
#endif
    if (!m_window) {
        SDL_Log("Window creation failed: %s", SDL_GetError());
        return false;
    }

    // Always use software renderer on MUOS (no GPU)
    m_renderer = SDL_CreateRenderer(m_window, -1, SDL_RENDERER_SOFTWARE);
    if (!m_renderer) {
        SDL_Log("Renderer creation failed: %s", SDL_GetError());
        return false;
    }

    // Render texture at internal resolution
    m_texture = SDL_CreateTexture(
        m_renderer,
        SDL_PIXELFORMAT_ARGB8888,
        SDL_TEXTUREACCESS_STREAMING,
        RENDER_W, RENDER_H
    );

    m_pixels = new uint32_t[RENDER_W * RENDER_H];
    memset(m_pixels, 0, RENDER_W * RENDER_H * sizeof(uint32_t));
    m_lastTick = SDL_GetTicks();
    m_frameCount = 0;

    SDL_Log("%s initialized (%dx%d render, %dx%d window)", APP_NAME, RENDER_W, RENDER_H, SCREEN_W, SCREEN_H);
    return true;
}

bool Renderer::shouldQuit() const {
    return !g_running;
}

void Renderer::shutdown() {
    delete[] m_pixels;
    if (m_texture)  SDL_DestroyTexture(m_texture);
    if (m_renderer) SDL_DestroyRenderer(m_renderer);
    if (m_window)   SDL_DestroyWindow(m_window);
    SDL_Quit();
}

void Renderer::beginFrame() {
    m_frameStart = SDL_GetTicks();
    uint32_t now = m_frameStart;
    m_dt = (now - m_lastTick) / 1000.0f;
    // Clamp dt to prevent spiral of death on slow frames
    if (m_dt > 0.1f) m_dt = 0.1f;
    m_lastTick = now;
    clear();
}

void Renderer::endFrame() {
    // Upload pixel buffer to texture, then present scaled
    SDL_UpdateTexture(m_texture, nullptr, m_pixels, RENDER_W * sizeof(uint32_t));
    SDL_RenderClear(m_renderer);
    SDL_RenderCopy(m_renderer, m_texture, nullptr, nullptr);
    SDL_RenderPresent(m_renderer);
    m_frameCount++;

    // Strict frame rate limiting
    uint32_t frameTime = SDL_GetTicks() - m_frameStart;
    uint32_t targetTime = 1000 / TARGET_FPS;
    if (frameTime < targetTime) {
        SDL_Delay(targetTime - frameTime);
    }
}

void Renderer::clear(Color c) {
    uint32_t val = c.toSDL();
    for (int i = 0; i < RENDER_W * RENDER_H; i++) {
        m_pixels[i] = val;
    }
}

void Renderer::pixel(int x, int y, Color c) {
    if (x >= 0 && x < RENDER_W && y >= 0 && y < RENDER_H) {
        m_pixels[y * RENDER_W + x] = c.toSDL();
    }
}

void Renderer::line(int x1, int y1, int x2, int y2, Color c) {
    // Bresenham's line
    int dx = abs(x2 - x1), sx = x1 < x2 ? 1 : -1;
    int dy = -abs(y2 - y1), sy = y1 < y2 ? 1 : -1;
    int err = dx + dy;

    while (true) {
        pixel(x1, y1, c);
        if (x1 == x2 && y1 == y2) break;
        int e2 = 2 * err;
        if (e2 >= dy) { err += dy; x1 += sx; }
        if (e2 <= dx) { err += dx; y1 += sy; }
    }
}

void Renderer::rect(int x, int y, int w, int h, Color c, bool filled) {
    if (filled) {
        for (int iy = y; iy < y + h; iy++)
            for (int ix = x; ix < x + w; ix++)
                pixel(ix, iy, c);
    } else {
        line(x, y, x + w - 1, y, c);
        line(x, y + h - 1, x + w - 1, y + h - 1, c);
        line(x, y, x, y + h - 1, c);
        line(x + w - 1, y, x + w - 1, y + h - 1, c);
    }
}

void Renderer::circle(int cx, int cy, int r, Color c, bool filled) {
    // Midpoint circle
    int x = r, y = 0, d = 1 - r;

    auto drawHLine = [&](int x1, int x2, int y) {
        if (x1 > x2) std::swap(x1, x2);
        for (int i = x1; i <= x2; i++) pixel(i, y, c);
    };

    while (x >= y) {
        if (filled) {
            drawHLine(cx - x, cx + x, cy + y);
            drawHLine(cx - x, cx + x, cy - y);
            drawHLine(cx - y, cx + y, cy + x);
            drawHLine(cx - y, cx + y, cy - x);
        } else {
            pixel(cx + x, cy + y, c);
            pixel(cx - x, cy + y, c);
            pixel(cx + x, cy - y, c);
            pixel(cx - x, cy - y, c);
            pixel(cx + y, cy + x, c);
            pixel(cx - y, cy + x, c);
            pixel(cx + y, cy - x, c);
            pixel(cx - y, cy - x, c);
        }
        y++;
        if (d <= 0) {
            d += 2 * y + 1;
        } else {
            x--;
            d += 2 * (y - x) + 1;
        }
    }
}

void Renderer::bar(int x, int y, int w, int h, Color c) {
    rect(x, y, w, h, c, true);
}

void Renderer::charGlyph(int x, int y, char ch, Color c, int scale) {
    int idx = ch - 32;
    if (idx < 0 || idx > 94) return;

    const uint8_t* glyph = FONT_4X6[idx];
    for (int row = 0; row < 6; row++) {
        for (int col = 0; col < 4; col++) {
            if (glyph[row] & (0x8 >> col)) {
                if (scale == 1) {
                    pixel(x + col, y + row, c);
                } else {
                    rect(x + col * scale, y + row * scale, scale, scale, c, true);
                }
            }
        }
    }
}

void Renderer::text(int x, int y, const std::string& str, Color c, int scale) {
    int cx = x;
    for (char ch : str) {
        if (ch == '\n') {
            y += 7 * scale;
            cx = x;
            continue;
        }
        charGlyph(cx, y, ch, c, scale);
        cx += 5 * scale;
    }
}

void Renderer::textCentered(int y, const std::string& str, Color c, int scale) {
    int w = (int)str.length() * 5 * scale - scale;
    text((RENDER_W - w) / 2, y, str, c, scale);
}
